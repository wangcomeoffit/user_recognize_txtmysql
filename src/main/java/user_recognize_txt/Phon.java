package user_recognize_txt;

public class Phon {
        private String gr;
        private String ph;
        private double s;
        private int l;
        private String t;
        private boolean f;
        public void setGr(String gr) {
            this.gr = gr;
        }
        public String getGr() {
            return gr;
        }

        public void setPh(String ph) {
            this.ph = ph;
        }
        public String getPh() {
            return ph;
        }

        public void setS(double s) {
            this.s = s;
        }
        public double getS() {
            return s;
        }

        public void setL(int l) {
            this.l = l;
        }
        public int getL() {
            return l;
        }

        public void setT(String t) {
            this.t = t;
        }
        public String getT() {
            return t;
        }

        public void setF(boolean f) {
            this.f = f;
        }
        public boolean getF() {
            return f;
        }
}
